public class Restaurant {

    //Attributes

    String restaurantName;
    String restaurantArea;
    String restaurantNumber;
    String meal1;
    double meal1Price;
    String meal2;
    double meal2Price;
    String meal3;
    double meal3Price;

    //Constructor

    public Restaurant(String restaurantName, String restaurantArea, String restaurantNumber, String meal1,
                      double meal1Price, String meal2, double meal2Price, String meal3, double meal3Price) {

        this.restaurantName = restaurantName;
        this.restaurantArea = restaurantArea;
        this.restaurantNumber = restaurantNumber;
        this.meal1 = meal1;
        this.meal1Price = meal1Price;
        this.meal2 = meal2;
        this.meal2Price = meal2Price;
        this.meal3 = meal3;
        this.meal3Price = meal3Price;


    }


}
